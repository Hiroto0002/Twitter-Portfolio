// **********************************************
// **********************************************

// サーバーを指定したポート番号で起動します
app.listen(PORT, () => {
  console.log(`サーバーが起動しました: http://localhost:${PORT}`);
  console.log('ブラウザでこのアドレスにアクセスしてみてください。');
});